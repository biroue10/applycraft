#!/usr/bin/env node
// Generates public/consent.js — the standalone cookie-consent + GA4 loader.
//
// Why a generated vanilla asset instead of a React component: the banner has
// to behave identically on the 75 prerendered static pages under public/ and
// inside the React app, and it must run before any Google request is made.
// A single dependency-free script loaded from <head> is the only thing that
// satisfies both. Strings still come from the i18n namespaces, so this stays
// the single source of truth — never hand-edit public/consent.js.
//
// Run via `npm run seo:pages`.
import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

import en from "../src/i18n/namespaces/en/consent.js";
import fr from "../src/i18n/namespaces/fr/consent.js";
import ar from "../src/i18n/namespaces/ar/consent.js";
import { GA_MEASUREMENT_ID } from "../src/config.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, "..", "public", "consent.js");

const GA_ID = GA_MEASUREMENT_ID;
const STRINGS = { en, fr, ar };

const asset = `/* GENERATED by scripts/generate-consent-asset.mjs — do not edit by hand.
 * Strings live in src/i18n/namespaces/<lang>/consent.js.
 *
 * Google Analytics is NOT loaded until the visitor accepts. Consent Mode v2
 * defaults are set to denied before anything else, so even the queued state
 * is correct if a tag is ever added elsewhere.
 */
(function () {
  "use strict";

  var GA_ID = ${JSON.stringify(GA_ID)};
  var STORAGE_KEY = "ac_cookie_consent";
  var RTL_LANGS = ["ar"];
  var T = ${JSON.stringify(STRINGS, null, 2)};

  function lang() {
    var raw = (document.documentElement.getAttribute("lang") || "en").toLowerCase();
    var code = raw.split("-")[0];
    return T[code] ? code : "en";
  }

  function t(key) {
    var code = lang();
    return (T[code] && T[code][key]) || T.en[key] || key;
  }

  function read() {
    try { return localStorage.getItem(STORAGE_KEY); } catch (e) { return null; }
  }

  function write(value) {
    try { localStorage.setItem(STORAGE_KEY, value); } catch (e) { /* private mode */ }
  }

  // ── Consent Mode v2 defaults — must run before any gtag activity ─────────
  window.dataLayer = window.dataLayer || [];
  function gtag() { window.dataLayer.push(arguments); }
  window.gtag = window.gtag || gtag;

  gtag("consent", "default", {
    ad_storage: "denied",
    ad_user_data: "denied",
    ad_personalization: "denied",
    analytics_storage: "denied",
  });

  var gaLoaded = false;

  function loadGA() {
    if (gaLoaded) return;
    gaLoaded = true;
    var s = document.createElement("script");
    s.async = true;
    s.src = "https://www.googletagmanager.com/gtag/js?id=" + encodeURIComponent(GA_ID);
    document.head.appendChild(s);
    gtag("js", new Date());
    gtag("config", GA_ID, { anonymize_ip: true });
  }

  function grant() {
    gtag("consent", "update", { analytics_storage: "granted" });
    loadGA();
  }

  function deny() {
    gtag("consent", "update", { analytics_storage: "denied" });
    // Best-effort removal of any _ga cookies from a previously granted visit.
    // GA cannot be unloaded once running, but consent mode stops collection.
    try {
      var host = location.hostname.replace(/^www\\./, "");
      document.cookie.split(";").forEach(function (pair) {
        var name = pair.split("=")[0].trim();
        if (name.indexOf("_ga") !== 0) return;
        document.cookie = name + "=; Max-Age=0; path=/";
        document.cookie = name + "=; Max-Age=0; path=/; domain=." + host;
      });
    } catch (e) { /* nothing we can do */ }
  }

  // ── Banner ───────────────────────────────────────────────────────────────
  var node = null;

  function close() {
    if (node && node.parentNode) node.parentNode.removeChild(node);
    node = null;
  }

  function show() {
    close();
    var rtl = RTL_LANGS.indexOf(lang()) !== -1;

    node = document.createElement("div");
    node.id = "ac-consent";
    node.setAttribute("role", "dialog");
    node.setAttribute("aria-label", t("regionLabel"));
    node.setAttribute("dir", rtl ? "rtl" : "ltr");
    node.style.cssText = [
      "position:fixed", "z-index:2147483000", "inset-inline:16px", "bottom:16px",
      "max-width:560px", "margin-inline:auto",
      "background:#0D1117", "border:1px solid #1A2740", "border-radius:12px",
      "padding:20px", "box-shadow:0 12px 40px rgba(0,0,0,.5)",
      "font-family:'IBM Plex Sans',system-ui,sans-serif",
      "color:#8B9EB8", "font-size:13.5px", "line-height:1.7",
      "text-align:" + (rtl ? "right" : "left"),
    ].join(";");

    var title = document.createElement("div");
    title.textContent = t("title");
    title.style.cssText = "font-size:15px;font-weight:700;color:#E4EBF5;margin-bottom:8px";

    var body = document.createElement("p");
    body.textContent = t("body");
    body.style.cssText = "margin:0 0 14px";

    var link = document.createElement("a");
    link.href = lang() === "en" ? "/privacy/" : "/" + lang() + "/privacy/";
    link.textContent = t("privacyLink");
    link.style.cssText = "color:#818CF8;font-size:13px;text-decoration:underline";

    var row = document.createElement("div");
    row.style.cssText = "display:flex;gap:10px;flex-wrap:wrap;align-items:center;margin-top:14px";

    // Reject is styled with the same size and prominence as accept —
    // GDPR requires refusing to be as easy as consenting.
    var base = "flex:1 1 160px;padding:11px 18px;border-radius:8px;font-size:13.5px;" +
      "font-weight:700;cursor:pointer;font-family:inherit;border:1px solid";

    var reject = document.createElement("button");
    reject.type = "button";
    reject.textContent = t("reject");
    reject.style.cssText = base + " #2A3A55;background:#161C26;color:#E4EBF5";
    reject.addEventListener("click", function () {
      write("denied");
      deny();
      close();
    });

    var accept = document.createElement("button");
    accept.type = "button";
    accept.textContent = t("accept");
    accept.style.cssText = base + " #6366F1;background:#6366F1;color:#fff";
    accept.addEventListener("click", function () {
      write("granted");
      grant();
      close();
    });

    row.appendChild(reject);
    row.appendChild(accept);
    node.appendChild(title);
    node.appendChild(body);
    node.appendChild(link);
    node.appendChild(row);
    document.body.appendChild(node);
    reject.focus();
  }

  // Footer "Cookie preferences" entry point.
  window.acCookiePreferences = show;

  function start() {
    var stored = read();
    if (stored === "granted") grant();
    else if (stored !== "denied") show();

    // Wire any footer trigger rendered by the static or React footer.
    document.addEventListener("click", function (event) {
      var trigger = event.target.closest && event.target.closest("[data-ac-cookie-prefs]");
      if (!trigger) return;
      event.preventDefault();
      show();
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", start);
  } else {
    start();
  }
})();
`;

writeFileSync(OUT, asset, "utf8");
console.log(`✓ consent asset written: public/consent.js (${Object.keys(STRINGS).join(", ")})`);
