export function documentLabelsFor(language) {
  const code = String(language || "en").toLowerCase().split("-")[0];
  return DOCUMENT_LABELS[code] || DOCUMENT_LABELS.en;
}

export function sectionLabel(language, key) {
  return documentLabelsFor(language).sections[key] || DOCUMENT_LABELS.en.sections[key] || key;
}
